const contractAddress = "0x974f83bFcf390d21ADB95d1cD82eBC5Ea5B468f8";

export const initWeb3 = async (setAccount, setProvider, setContract, setBalance) => {
    try {
      // Check if MetaMask is installed in the browser
      if (typeof window.ethereum !== 'undefined') {
        // Create a Web3 instance with the current provider
        const web3 = new Web3(window.ethereum);
        setProvider(web3);
  
        // Request access to the user's MetaMask account
        const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
        const account = accounts[0]; // Get the first account (the active account)
        setAccount(account);
  
        // Create a contract instance using the contract ABI and address
        const contract = new web3.eth.Contract(contractABI, contractAddress);
        setContract(contract);
  
        // Get the user's balance in Wei (convert it to Ether for display)
        const balanceWei = await web3.eth.getBalance(account);
        const balanceEther = web3.utils.fromWei(balanceWei, 'ether'); // Convert from Wei to Ether
        setBalance(balanceEther);
      } else {
        console.error('MetaMask is not installed!');
      }
    } catch (error) {
      console.error('Error initializing Web3:', error);
    }
  };